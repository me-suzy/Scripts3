                        <br><br> - Importer/exporter vos adresses vers/depuis votre logiciel email ou autre application de gestion des adresses à l'aide d'un fichier texte avec séparateur
                        <br><br> - Gérer des groupes afin de permettre aux membres d'un groupe de partager toute ou partie des contacts. Si, dans une association, chacun est responsable de son adresse, la qualité du contenu partagé est meilleure.
                        <br><br> - D'envoyer un email aux membres d'un groupe. Petite fonction de Mailing list qui ne remplacera pas un outils dédié, mais qui peut rendre service.
                        <br><br> - wa-boo est finement configurable (couleurs, polices, organisation des informations, Langues)  
