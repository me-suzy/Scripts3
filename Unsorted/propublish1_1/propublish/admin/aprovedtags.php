Siden Webber.no spesiellt er beregnet for artikler relatert til kode,
filtreres html og php ut og vises som kode i stedet for å vise
layoutmessig. Derfor må spesiell html kode benyttes i stedet for 
tradisjonell kode, [b] erstatter &ltb&gt;, for eksempel.
<p>
<u>Eksempel på tegn:</u><p>
<table width=100% border=0 cellspacing="1" cellpadding="3">
