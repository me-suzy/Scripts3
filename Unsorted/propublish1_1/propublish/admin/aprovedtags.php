<tr><td>&ltimage&gt;23&lt/image&gt</td><td>(Viser opplastet bilde fra db)<br></td></tr>
<tr><td>&ltimage_left&gt;23&lt/image&gt</td><td>(Viser opplastet bilde fra db)<br></td></tr>
<tr><td>&ltimage_right&gt;23&lt/image&gt</td><td>(Viser opplastet bilde fra db)<br></td></tr>
<tr><td>[b]Dette er en fet skrift[/b]</td><td><b>Dette er en fet skrift</b><br></td></tr>
<tr><td>[url]http://www.sol.no[/url]</td><td><a href="http://www.sol.no">Link</a><br></td>
<tr><td>[i]Dette er en ingress skrift[/i]</td><td><i>Dette er ingress skrift</i><br></td></tr>
<tr><td>[u]Dette er en understreket skrift[/u]</td><td><u>Dette er understreket skrift</u><br></td></tr>
